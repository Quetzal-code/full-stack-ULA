# Diferentes tipos de datos aparte de los vistos en clase 
# String, Boolean, Float, Integer 

# Listas (list):
# Son colecciones ordenadas de elementos que pueden ser de diferentes tipos. Las listas son mutables, lo que 
#significa que puedes cambiar sus elementos. Ejemplo: [1, 2, 3, "a", "b", "c"].


# Tuplas (tuple):
# Son similares a las listas, pero inmutables. Una vez creada una tupla, no puedes modificar sus elementos. 
#Ejemplo: (1, 2, 3).


# Diccionarios (dict):
# Son colecciones no ordenadas de pares clave-valor. Las claves deben ser únicas y pueden ser de diferentes 
#tipos. Ejemplo: {"nombre": "Ana", "edad": 25}.


# Conjuntos (set):
# Son colecciones no ordenadas y sin elementos duplicados. Son útiles para operaciones como la unión, 
#intersección, y diferencia. Ejemplo: {1, 2, 3}.