# Diagrama de flujos 
# Es como un mapa de instrucciones que te muestra el camino a seguir para completar una tarea, 
# usando flechas para indicar el orden de los pasos.


# Diagrama de secuencia 
# Muestra cómo diferentes partes de un sistema (como programas o usuarios) 
# interactúan entre sí, en qué orden ocurre la comunicación y qué se intercambia.


# Diagrama de actividades
# Similar a un diagrama de flujos, pero más detallado, muestra el proceso de realizar una actividad, 
# incluyendo decisiones y diferentes caminos que se pueden tomar.


# Diagrama de caso de uso
# Describe cómo los usuarios interactúan con un sistema, mostrando las diferentes acciones que pueden 
# realizar y cómo el sistema responde.